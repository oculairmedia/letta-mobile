    @Test
    fun `test LettaMessage serialization round trip for ToolCallMessage`() {
        val jsonStr = """{
            "id": "tc1",
            "message_type": "tool_call",
            "tool_call": {
                "name": "search",
                "arguments": "{\"q\": \"hello\"}",
                "tool_call_id": "call_123"
            },
            "run_id": "r1",
            "seq_id": 5
        }"""
        
        val decoded = Json { ignoreUnknownKeys = true }.decodeFromString<LettaMessage>(jsonStr)
        assertTrue(decoded is ToolCallMessage)
        assertEquals("tc1", decoded.id)
        assertEquals("search", decoded.toolCall?.name)
        assertEquals("r1", decoded.runId)
        assertEquals(5, decoded.seqId)

        val encoded = Json { ignoreUnknownKeys = true }.encodeToString(LettaMessage.serializer(), decoded)
        val decodedAgain = Json { ignoreUnknownKeys = true }.decodeFromString<LettaMessage>(encoded)
        assertTrue(decodedAgain is ToolCallMessage)
        assertEquals(decoded.id, decodedAgain.id)
        assertEquals(decoded.toolCall?.name, decodedAgain.toolCall?.name)
    }
}
