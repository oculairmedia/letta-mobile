    @Test
    fun `test renderToolCallContent`() {
        val empty = renderToolCallContent(emptyList())
        assertEquals("tool_call", empty)

        val single = renderToolCallContent(listOf(ToolCall(name = "search", arguments = "{\"q\": \"hello\"}")))
        assertEquals("search({\"q\": \"hello\"})", single)

        val multiple = renderToolCallContent(listOf(
            ToolCall(name = "t1", arguments = "a1"),
            ToolCall(name = "t2", arguments = "")
        ))
        assertEquals("t1(a1)\nt2", multiple)
    }

    @Test
    fun `test normalizeHydratedMessageOrder`() {
        val msg1 = UserMessage(id = "msg1", seqId = 10, contentRaw = JsonPrimitive(""), runId = "r1")
        val msg2 = AssistantMessage(id = "msg2", seqId = null, contentRaw = JsonPrimitive(""), runId = "r1")
        val msg3 = AssistantMessage(id = "msg3", seqId = 5, contentRaw = JsonPrimitive(""), runId = "r1")
        val msg4 = ToolCallMessage(id = "msg4", seqId = null, runId = "r1")

        val sorted = normalizeHydratedMessageOrder(listOf(msg2, msg1, msg4, msg3))
        
        assertEquals(4, sorted.size)
        // Original logic evaluation:
        // left=msg2 vs right=msg1 -> seqCompare=0, dateCompare=0. msg2 (Assistant, 4) vs msg1(User, 0) -> msg1 first.
        // After sorting:
        assertEquals("msg1", sorted[0].id) // USER
        assertEquals("msg4", sorted[1].id) // TOOL
        assertEquals("msg2", sorted[2].id) // ASSISTANT (null seq fallback)
        assertEquals("msg3", sorted[3].id) // ASSISTANT (seq 5 => index preservation or seq? wait, actually let's hardcode the exact true order generated by the algorithm so we enforce stability)
    }

    @Test
    fun `test hasAlreadyIngestedStreamFrame`() {
        val baseEvent = TimelineEvent.Confirmed(
            position = 1.0, otid = "o1", content = "Hello world", serverId = "s1", 
            messageType = TimelineMessageType.ASSISTANT, date = timelineNow(),
            runId = "r1", stepId = null, seqId = 10
        )

        val inc1 = TimelineEvent.Confirmed(
            position = 2.0, otid = "o1", content = "Hello ", serverId = "s1",
            messageType = TimelineMessageType.ASSISTANT, date = timelineNow(),
            runId = "r1", stepId = null, seqId = 9
        )
        assertTrue(baseEvent.hasAlreadyIngestedStreamFrame(inc1))

        val inc2 = TimelineEvent.Confirmed(
            position = 2.0, otid = "o1", content = "world", serverId = "s1",
            messageType = TimelineMessageType.ASSISTANT, date = timelineNow(),
            runId = "r1", stepId = null, seqId = 8
        )
        assertTrue(baseEvent.hasAlreadyIngestedStreamFrame(inc2))

        val inc3 = TimelineEvent.Confirmed(
            position = 2.0, otid = "o1", content = "Hello world", serverId = "s1",
            messageType = TimelineMessageType.ASSISTANT, date = timelineNow(),
            runId = "r1", stepId = null, seqId = 10
        )
        assertTrue(baseEvent.hasAlreadyIngestedStreamFrame(inc3))

        val inc4 = TimelineEvent.Confirmed(
            position = 2.0, otid = "o1", content = "Hello world!", serverId = "s1",
            messageType = TimelineMessageType.ASSISTANT, date = timelineNow(),
            runId = "r1", stepId = null, seqId = 5
        )
        assertFalse(baseEvent.hasAlreadyIngestedStreamFrame(inc4))
    }

