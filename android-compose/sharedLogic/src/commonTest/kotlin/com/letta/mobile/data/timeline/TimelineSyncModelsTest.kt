package com.letta.mobile.data.timeline

import com.letta.mobile.data.model.*
import kotlin.test.*
import kotlinx.serialization.json.Json
import kotlinx.serialization.json.JsonPrimitive

class TimelineSyncModelsTest {
    
    @Test
    fun `test toTimelineEvent for UserMessage`() {
        val msg = UserMessage(id = "msg1", contentRaw = JsonPrimitive("u_text"), runId = "r1", date = "2024-01-01T10:00:00Z", otid = "client_otid")
        val event = msg.toTimelineEvent(1.5)
        assertNotNull(event)
        assertEquals(TimelineMessageType.USER, event.messageType)
        assertEquals("u_text", event.content)
        assertEquals("client_otid", event.otid)
        assertEquals("msg1", event.serverId)
        assertEquals("r1", event.runId)
        assertEquals(1.5, event.position)
    }

    @Test
    fun `test toTimelineEvent for AssistantMessage`() {
        val msg = AssistantMessage(id = "msg2", contentRaw = JsonPrimitive("a_text"), runId = "r1")
        val event = msg.toTimelineEvent(2.0)
        assertNotNull(event)
        assertEquals(TimelineMessageType.ASSISTANT, event.messageType)
        assertEquals("a_text", event.content)
        assertEquals("server-msg2-assistant-r1", event.otid)
        assertEquals("msg2", event.serverId)
    }

    @Test
    fun `test toTimelineEvent preserves legacy server otid when run id is absent`() {
        val msg = AssistantMessage(id = "msg2", contentRaw = JsonPrimitive("a_text"))
        val event = msg.toTimelineEvent(2.0)

        assertNotNull(event)
        assertEquals("server-msg2-assistant", event.otid)
    }

    @Test
